local function DoT(i)
	local x="x"..i
	return {
		i.." rounds", function(s)
			return strfind(s, x.." %^")
			or strfind(s, x.."{[^}]*^")
			or strfind(s, x.." [be] ")
			or strfind(s, x.."{[^}]*[be] ")
			or strfind(s, x.." !@H%-",1,YES)
			or strfind(s, x.."{[^}]*!@H%-",1,YES)
		end
	}
end

local function attacks(i)
	return {tostring(i), function(s)
		local function f()
			local n=0
			for _ in string.gmatch(s,"%^") do
				n=n+1
			end
			return n
		end
		local rpt = strmatch(s,"^%*(%d)")
		if rpt then
			return i == tonumber(rpt)
		else
			return i == f()
		end
	end}
end

